// Método .forEach()
// ele é usado para executar uma dada função em cada elemento de um array.

// primeiro exemplo: escola;
let escola = ["lápis","caneta", "borracha" ];
escola.forEach((element) => console.log(element));
// neste exemplo ele executou cada elemnto que estava dentro do array.

// segundo exemplo: matérias;
let matérias = ["português", "matemática", "artes", "biologia", "geografia"];
matérias.forEach((element) => console.log(element));