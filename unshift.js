// Método .unshift()
// ele é usado para adicionar um ou mais elementos no ínicio de um array.

// primeiro exemplo: frutas;
let frutas = ["maçã", "banana", "manga", "melancia", "goiaba"];
frutas.unshift("abacaxi", "morango");

// foi adicionado as frutas: acabaxi e morango no ínicio do array.
console.log(frutas);

// segundo exemplo: números;
let numero = ["4", "5", "6"];
numero.unshift("1", "2", "3");

// foi adicionado os números: 1, 2 e 3 no ínicio do array.
console.log(numero);